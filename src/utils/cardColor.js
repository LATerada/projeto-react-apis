export function cardColor(pokemonType) {
  if (pokemonType === "bug") {
    return "#76A866";
  }
  if (pokemonType === "dark") {
    return "#729F92";
  }
  if (pokemonType === "dragon") {
    return "#004170";
  }
  if (pokemonType === "electric") {
    return "#F9DD6C";
  }
  if (pokemonType === "fairy") {
    return "#E7ACC9";
  }
  if (pokemonType === "fighting") {
    return "#E5706C";
  }
  if (pokemonType === "fire") {
    return "#EAAB7D";
  }
  if (pokemonType === "flying") {
    return "#A98FF3";
  }
  if (pokemonType === "ghost") {
    return "#735797";
  }
  if (pokemonType === "grass") {
    return "#729F92";
  }
  if (pokemonType === "ground") {
    return "#E2BF65";
  }
  if (pokemonType === "ice") {
    return "#96D9D6";
  }
  if (pokemonType === "normal") {
    return "#BF9762";
  }
  if (pokemonType === "poison") {
    return "#8D4f8C";
  }
  if (pokemonType === "psychic") {
    return "#EF769A";
  }
  if (pokemonType === "rock") {
    return "#B6A136";
  }
  if (pokemonType === "steel") {
    return "#B7B7CE";
  }
  if (pokemonType === "water") {
    return "#71C3FF";
  }
}
