export function cardColor (pokemonType){
    if(pokemonType === 'grass'){
        return '#729F92';
    }
    if(pokemonType === 'fire'){
        return '#EAAB7D'
    }
    if(pokemonType === 'water'){
        return '#71C3FF'
    }
    if(pokemonType === 'bug'){
        return '#76A866'
    }
    if(pokemonType === 'normal'){
        return '#BF9762'
    }

}